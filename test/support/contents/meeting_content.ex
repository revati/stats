     		<p><strong>Latvijas Republikas 12.Saeimas<br /> ziemas sesijas pirmā sēde<br /> 2018.gada 11.janvārī </strong></p>
     <p>Sēdi vada Latvijas Republikas 12.Saeimas priek&scaron;sēdētājas biedrs<br /> Gundars Daudze.</p>
     <p><a href="http://titania.saeima.lv/LIVS12/SaeimaLIVS2_DK.nsf/DK?ReadForm&amp;nr=fc3ebefe-4316-4bcc-855c-5f6f68026ea5" target="_new"><em>Sēdes darba kārtība</em></a><br /> <a href="#sat"><em>Satura rādītājs</em></a><br /> <a href="#Bals"><em>Balsojumi</em></a><br /> <a href="/lv/aktualitates/12-saeimas-frakciju-viedokli/26492-frakciju-viedokli-2018-gada-11-janvari" target="_new"><em>Frakciju viedokļi</em></a></p>
     <p>Sēdes vadītājs. Labrīt, godātie kolēģi! Pulkstenis ir 9.00. Lūdzu ieņemt vietas! Sāksim Saeimas 2018.gada 11.janvāra sēdi. Tā kā &scaron;ī ir pirmā sēde &scaron;ī gada ziemas sesijā, ļaujiet man jums visiem vēlēt laimīgu jauno gadu, un lai izdodas piepildīt visas tās ieceres, kuras jūs esat iecerēju&scaron;i &scaron;im gadam!</p>
     <p><a name="s1"></a></p>
     <p>Bet tagad par sēdes darba kārtību. Pirms mēs sākam izskatīt apstiprināto &scaron;odienas darba kārtību, mums jālemj par iespējamiem grozījumiem tajā.</p>
     <p><a name="LM0650_02"></a></p>
